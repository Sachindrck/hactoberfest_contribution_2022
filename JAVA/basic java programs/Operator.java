//using combinational arithmetic operators
import java.util.*;
class Operator{
public static void main(String args[]){
int a=4,b=8,c=9,d=12;
int e=7,f=10;
b+=a;
f-=e;
d/=3;
c%=a;
e*=3;
System.out.println("b="+b);
System.out.println("c="+c+"\td="+d);
System.out.println("e="+e+"\tf="+f);

}
}